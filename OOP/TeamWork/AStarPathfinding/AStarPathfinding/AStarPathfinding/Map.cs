        /// <summary>
        /// Returns the tile index for the given cell.
        /// </summary>
        public int GetValue(int cellX, int cellY)
        {
            if (cellX < 0 || cellX > Width - 1 || cellY < 0 || cellY > Height - 1)
            {
                return 0;
            }

            return layout[cellX, cellY];
        }

        /// <summary>
        /// Returns specified vector on which cell of the map grid is placed.
        /// </summary>
        private Point VectorToCell(Vector2 vector)
        {
            return new Point((int)vector.X / this.tileWidth, (int)vector.Y / this.tileHeight);
        }

        /// <summary>
        /// Returns the size of the window plus one tile dimensions. 
        /// </summary>
        /// <returns></returns>
        private Vector2 ViewPortVector()
        {
            return new Vector2(screenWidth + this.tileWidth, screenHeight + this.tileHeight);
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            // Coordinates of the top left tile of current view
            Point cameraPosition = VectorToCell(this.camera.Position);

            // Coordinates of the bottom right tile of current view
            Point viewPoint = VectorToCell(this.camera.Position + ViewPortVector());

            Point min = new Point();
            Point max = new Point();

            min.X = cameraPosition.X;
            min.Y = cameraPosition.Y;
            max.X = Math.Min(viewPoint.X, layout.GetLength(1));
            max.Y = Math.Min(viewPoint.Y, layout.GetLength(0));

            Rectangle tileRectangle = new Rectangle(0, 0, this.tileWidth, this.tileHeight);

            for (int y = min.Y; y < max.Y; y++)
            {
                for (int x = min.X; x < max.X; x++)
                {
                    int index = layout[y, x];
                    tileRectangle.X = (x * tileWidth) - (int)this.camera.Position.X;
                    tileRectangle.Y = (y * tileHeight) - (int)this.camera.Position.Y;
                    spriteBatch.Draw(this.textures[index], tileRectangle, Color.White);
                }
            }
        }
