        /// <summary>
        /// Allows the game to run logic such as updating the world,
        /// checking for collisions, gathering input, and playing audio.
        /// </summary>
        /// <param name="gameTime">Provides a snapshot of timing values.</param>
        public void Update(GameTime gameTime)
        {
            this.currentState = Keyboard.GetState();

            // Allows the game to exit
            if (this.currentState.IsKeyDown(Keys.Escape))

            this.motion = Vector2.Zero;

            if (this.currentState.IsKeyDown(Keys.Up))
            {
                ScrollUp();
            }
            
            if (this.currentState.IsKeyDown(Keys.Down))
            {
                ScrollDown();
            }

            if(this.currentState.IsKeyDown(Keys.Left))
            {
                ScrollLeft();
            }

            if (this.currentState.IsKeyDown(Keys.Right))
            {
                ScrollRight();
            }

            if (this.motion != Vector2.Zero)
            {
                this.motion.Normalize();
                this.camera.Position += this.motion * this.camera.Speed;
            }
        }

        private void ScrollUp()
        {
            this.motion.Y = -1;
        }

        private void ScrollDown()
        {
            this.motion.Y = 1;
        }

        private void ScrollLeft()
        {
            this.motion.X = -1;
        }

        private void ScrollRight()
        {
            this.motion.X = 1;
        }
